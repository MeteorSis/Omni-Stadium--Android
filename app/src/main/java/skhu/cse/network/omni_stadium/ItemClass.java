package skhu.cse.network.omni_stadium;


public class ItemClass{
    private  String name;
    private  String cost;

    public  ItemClass(String name, String cost){
        this.name = name;
        this.cost = cost;
    }

    public  String getName() {
        return  name;
    }

    public String getCost() {
        return  cost;
    }

}
